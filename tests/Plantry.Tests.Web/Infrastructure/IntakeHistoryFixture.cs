/// <summary>
/// L4 WebApplicationFactory for the /Intake/History and /Intake/Session pages (receipt-intake-history.md
/// H5/H7). Swaps every Postgres-backed seam for an in-memory fake so both pages render without a database.
/// </summary>
public sealed class IntakeHistorySessionFragmentFactory : WebApplicationFactory<Program>
{
    public Unit ExistingUnit { get; } = Unit.Create(
        HouseholdId.From(IntakeHistoryFixture.HouseholdAId), "g", "Gram", Dimension.Mass, 1m, isBase: true);

    public Product ExistingProduct { get; }

    public ImportSession Committed { get; }
    public ImportSession Ready { get; } = IntakeHistoryFixture.BuildReady(IntakeHistoryFixture.HouseholdAId);
    public ImportSession Failed { get; } = IntakeHistoryFixture.BuildFailed(IntakeHistoryFixture.HouseholdAId);
    public ImportSession Discarded { get; } = IntakeHistoryFixture.BuildDiscarded(IntakeHistoryFixture.HouseholdAId);
    public ImportSession ForeignCommitted { get; }

    public IntakeHistorySessionFragmentFactory()
    {
        ExistingProduct = Product.Create(
            HouseholdId.From(IntakeHistoryFixture.HouseholdAId), "Baby spinach", ExistingUnit.Id, SystemClock.Instance);
        Committed = IntakeHistoryFixture.BuildCommitted(IntakeHistoryFixture.HouseholdAId, ExistingProduct.Id.Value, ExistingUnit.Id.Value);
        ForeignCommitted = IntakeHistoryFixture.BuildCommitted(IntakeHistoryFixture.HouseholdBId, ExistingProduct.Id.Value, ExistingUnit.Id.Value);
    }

    protected override void ConfigureWebHost(IWebHostBuilder builder)
    {
        builder.UseEnvironment("Testing");

        builder.ConfigureTestServices(services =>
        {
            services.AddFakeDisplayCurrency();
            services.AddAuthentication(options =>
                {
                    options.DefaultScheme = TestAuthHandler.SchemeName;
                    options.DefaultAuthenticateScheme = TestAuthHandler.SchemeName;
                    options.DefaultChallengeScheme = TestAuthHandler.SchemeName;
                })
                .AddScheme<AuthenticationSchemeOptions, TestAuthHandler>(TestAuthHandler.SchemeName, _ => { });

            services.RemoveAll<IImportSessionRepository>();
            services.AddScoped<IImportSessionRepository>(sp => new MultiSessionImportSessionRepository(
                sp.GetRequiredService<ITenantContext>(), Committed, Ready, Failed, Discarded, ForeignCommitted));

            services.RemoveAll<Plantry.Catalog.Domain.IProductRepository>();
            services.AddScoped<Plantry.Catalog.Domain.IProductRepository>(
                _ => new FakeSessionProductRepository(ExistingProduct));

            services.RemoveAll<Plantry.Catalog.Domain.IUnitRepository>();
            services.AddScoped<Plantry.Catalog.Domain.IUnitRepository>(
                _ => new FakeSessionUnitRepository(ExistingUnit));

            services.RemoveAll<IProductStockRepository>();
            services.AddScoped<IProductStockRepository>(
                _ => new FakeSessionStockRepository(ExistingProduct.Id.Value));

            services.RemoveAll<IHouseholdMemberReader>();
            services.AddScoped<IHouseholdMemberReader, FakeSessionMemberReader>();
        });
    }

    public HttpClient CreateAuthClient(Guid householdId)
    {
        var client = CreateClient();
        client.DefaultRequestHeaders.Add(TestAuthHandler.HouseholdHeader, householdId.ToString());
        return client;
    }
}
